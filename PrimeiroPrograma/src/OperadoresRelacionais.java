public class OperadoresRelacionais {

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 20;

        System.out.println(num1>num2); // falso
        System.out.println(num1<num2); // verdadeiro
        System.out.println(num1>=num2); //falso
        System.out.println(num1<=num2); //verdadeiro
    }

}
